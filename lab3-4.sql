CREATE OR REPLACE PROCEDURE P_CALCULATE_SALARY_ALL (
    p_month IN VARCHAR2
) IS
    -- Створюємо курсор, який вибирає ПІБ усіх працівників
    CURSOR c_spivrobitnyky IS
        SELECT PRIZVYSHCHE, IMIA, PO_BATKOVI
        FROM SPIVROBITNYKY;
BEGIN
    -- Проходимо циклом по кожному працівнику з курсору
    FOR r_emp IN c_spivrobitnyky LOOP
        -- Викликаємо першу процедуру для поточного працівника в циклі
        P_CALCULATE_SALARY_SINGLE(
            r_emp.PRIZVYSHCHE, 
            r_emp.IMIA, 
            r_emp.PO_BATKOVI, 
            p_month
        );
    END LOOP;

    -- Фіксуємо зміни для всіх працівників одразу
    COMMIT;
    
    DBMS_OUTPUT.PUT_LINE('--- Нарахування за ' || p_month || ' завершено для всього штату ---');
END;
/


