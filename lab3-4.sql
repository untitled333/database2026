CREATE TABLE PRODAGI (
    ID_PRODAGU NUMBER PRIMARY KEY,
    NAZVA_USTANOVY VARCHAR2(100),
    NAZVA_LIKIV VARCHAR2(50),
    KILKIST NUMBER,
    DATA_PRODAGU DATE DEFAULT SYSDATE,

    FOREIGN KEY (NAZVA_USTANOVY) 
        REFERENCES APTECHNA_USTANOVA(NAZVA_USTANOVY),

    FOREIGN KEY (NAZVA_LIKIV) 
        REFERENCES PERELIK_LIKARSTV(NAZVA_LIKIV)
);

--Тригер(забороняє продаж, якщо недостатньо товару)

CREATE OR REPLACE TRIGGER TRG_CHECK_KILKIST
BEFORE INSERT ON PRODAGI
FOR EACH ROW
DECLARE
    v_kilkist NUMBER;
BEGIN
    SELECT KILKIST
    INTO v_kilkist
    FROM NAYAVNI
    WHERE NAZVA_USTANOVY = :NEW.NAZVA_USTANOVY
      AND NAZVA_LIKIV = :NEW.NAZVA_LIKIV;

    IF v_kilkist < :NEW.KILKIST THEN
        RAISE_APPLICATION_ERROR(-20001, 'Недостатня кількість товару!');
    END IF;
END;
/

--Тригер(Зменшує залишок після продажу)
CREATE OR REPLACE TRIGGER TRG_UPDATE_KILKIST
AFTER INSERT ON PRODAGI
FOR EACH ROW
BEGIN
    UPDATE NAYAVNI
    SET KILKIST = KILKIST - :NEW.KILKIST
    WHERE NAZVA_USTANOVY = :NEW.NAZVA_USTANOVY
      AND NAZVA_LIKIV = :NEW.NAZVA_LIKIV;
END;
/


