CREATE OR REPLACE PROCEDURE P_CALCULATE_SALARY_SINGLE (
    p_surname IN VARCHAR2,
    p_name    IN VARCHAR2,
    p_patron  IN VARCHAR2,
    p_month   IN VARCHAR2
) IS
    v_base_salary NUMBER;
    v_experience  NUMBER;
    v_total_calc  NUMBER(10, 2);
BEGIN
    SELECT p.OKLAD, s.TRUDOVYI_STAZH
    INTO v_base_salary, v_experience
    FROM SPIVROBITNYKY s
    JOIN POSADA p ON s.NAZVA_POSADY = p.NAZVA_POSADY
    WHERE s.PRIZVYSHCHE = p_surname 
      AND s.IMIA = p_name 
      AND s.PO_BATKOVI = p_patron;

    v_total_calc := v_base_salary + (v_base_salary * (v_experience * 0.02));

    UPDATE SPIVROBITNYKY
    SET NARAKHOVANO_ZA_MISIATS = p_month || ': ' || TO_CHAR(v_total_calc, '99990.99') || ' грн.'
    WHERE PRIZVYSHCHE = p_surname 
      AND IMIA = p_name 
      AND PO_BATKOVI = p_patron;

    DBMS_OUTPUT.PUT_LINE('Зарплату нараховано!');
END;
