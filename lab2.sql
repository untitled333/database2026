-- or, and і сортування з однією таблицею (відсортовані за стажем за спаданням)
SELECT Прізвище, Ім_я, Трудовий_стаж, Назва_посади
FROM Співробітники
WHERE (Трудовий_стаж > 3 AND Назва_посади = 'Провізор')
      OR Назва_посади = 'Завідувач'
ORDER BY Трудовий_стаж DESC;


-- з обч. полем і його вивід (переводить роки т. стажу в місяці і присвоює вік)
SELECT 
Прізвище,
Ім_я,
Трудовий_стаж,
Трудовий_стаж * 12 AS Стаж_у_місяцях,
YEAR(GETDATE()) - YEAR(Дата_народження) AS Вік
FROM Співробітники;


--на базі кількох таблиць з використанням сортування, накладенням умов зі зв’язками OR та AND(виводить інф. про тих працівників, які мають стаж > 3 і працюють в неділю)
SELECT 
С.Прізвище,
С.Ім_я,
С.Назва_посади,
А.Назва_установи,
А.Час_роботи
FROM Співробітники С
JOIN Аптечна_установа А 
ON С.Назва_установи = А.Назва_установи
WHERE С.Трудовий_стаж > 3 AND А.Чи_вихідний_у_неділю = 0
ORDER BY С.Трудовий_стаж;

--на базі кількох таблиць з типом поєднання Outer Join(показує всі аптеки, навіть якщо в них немає ліків)
SELECT 
А.Назва_установи,
П.Назва_ліків
FROM Аптечна_установа А
LEFT JOIN Наявні Н 
ON А.Назва_установи = Н.Назва_установи
LEFT JOIN Перелік_лікарств П
ON Н.Назва_ліків = П.Назва_ліків;

