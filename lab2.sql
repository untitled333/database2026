--EXISTS (показує аптеки де є хоча б один наявний препарат)
SELECT Назва_установи
FROM Аптечна_установа А
WHERE EXISTS (
    SELECT *
    FROM Наявні Н
    WHERE Н.Назва_установи = А.Назва_установи
);

--ANY
SELECT *
FROM Співробітники
WHERE Трудовий_стаж > ANY (
    SELECT Трудовий_стаж FROM Співробітники
);

--ALL(виведе працівника з найбільшим стажем)
SELECT *
FROM Співробітники
WHERE Трудовий_стаж >= ALL (
    SELECT Трудовий_стаж FROM Співробітники
);

-- з використанням підсумовування та групування. (рахує к-сть працівників у конкретній аптеці та їх середній стаж)
SELECT 
Назва_установи,
COUNT(*) AS Кількість_працівників,
AVG(Трудовий_стаж) AS Середній_стаж
FROM Співробітники
GROUP BY Назва_установи;

-- з використанням під-запитів в частині Where.(виводить ніявні ліки які є в аптеці подорожник)
SELECT *
FROM Перелік_лікарств
WHERE Назва_ліків IN (
    SELECT Назва_ліків
    FROM Наявні
    WHERE Назва_установи = 'Подорожник'
);

--з використанням під-запитів в частині From.(підзапит рахує працівників і виводить де їх > 1)
SELECT *
FROM (
    SELECT Назва_установи, COUNT(*) AS Кількість
    FROM Співробітники
    GROUP BY Назва_установи
) AS Підсумок
WHERE Кількість > 1;

--ієрархічний SELECT запит.(присвоює співроб. рівні і сортує за ними )
WITH Ієрархія AS (
    SELECT 
        Прізвище,
        Ім_я,
        Назва_посади,
        CASE 
            WHEN Назва_посади = 'Завідувач' THEN 1
            WHEN Назва_посади = 'Провізор' THEN 2
            WHEN Назва_посади = 'Фармацевт' THEN 3
        END AS Рівень
    FROM Співробітники
)

SELECT *
FROM Ієрархія
ORDER BY Рівень;

--	SELECT запит типу CrossTab.(перетворює рядки у колонки)
SELECT *
FROM (
    SELECT Назва_установи, Назва_ліків
    FROM Наявні
) AS SourceTable
PIVOT
(
    COUNT(Назва_ліків)
    FOR Назва_ліків IN ([Парацетамол],[Но-шпа],[Цитрамон])
) AS PivotTable;

